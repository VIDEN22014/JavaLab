public class Person {
    public int Id;
    public String firstName;
    public String lastName;
    public int age;
    public int height;

    Person(int Id, String firstName, String lastName, int age, int height) {
        this.Id = Id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.height = height;
    }
}
